let currentSlide = 0;

/* यह सामान्य प्रस्तुति-प्रवाह के लिए आधारभूत फ्रंट-एंड पासवर्ड सुरक्षा है;
   यह उच्च-सुरक्षा डेटा-सुरक्षा का विकल्प नहीं है। */
const passwords = {
  presentation: "folksong",
  prep: "noontime"
};

function openPasswordBox(type) {
  targetPage = type;
  const modal = document.getElementById("passwordModal");
  const title = document.getElementById("modalTitle");
  const hint = document.getElementById("modalHint");
  const input = document.getElementById("passwordInput");
  const message = document.getElementById("passwordMessage");

  title.textContent = type === "presentation" ? "प्रस्तुति प्रारंभ करने हेतु पासवर्ड दर्ज करें" : "मेरा तैयारी कक्ष खोलने हेतु पासवर्ड दर्ज करें";
  hint.textContent = "कृपया निर्धारित पासवर्ड लिखें।";
  input.value = "";
  message.textContent = "";
  modal.classList.remove("hidden");
  input.focus();
}

function closePasswordBox() {
  document.getElementById("passwordModal").classList.add("hidden");
}

function checkPassword() {
  const value = document.getElementById("passwordInput").value.trim();
  const message = document.getElementById("passwordMessage");

  if (value === passwords[targetPage]) {
    message.textContent = targetPage === "presentation"
      ? "सही पासवर्ड। प्रस्तुति खोली जा रही है।"
      : "सही पासवर्ड। तैयारी कक्ष खोला जा रहा है।";

    setTimeout(() => {
      window.location.href = targetPage === "presentation" ? "presentation.html" : "prep.html";
    }, 700);
  } else {
    message.textContent = "गलत पासवर्ड। कृपया पुनः प्रयास करें।";
  }
}

